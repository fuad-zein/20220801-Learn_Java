package FundamentalJava.Materi;

public class AritmaticsOperator2 {
  public static void main(String[] args) {
    /*
     * += contoh: x+=y sama dengan x = x+y
     * -= contoh: x-=y sama dengan x = x-y
     * *= contoh: x*=y sama dengan x = x*y
     * /= contoh: x/=y sama dengan x = x/y
     * %= contoh: x%=y sama dengan x = x%y
     */

    int x = 10;

    x += 2; // x = x+2
    System.out.println(x);
    x -= 2; // x = x-2
    System.out.println(x);
    x *= 2; // x = x*2
    System.out.println(x);
    x /= 2; // x = x/2
    System.out.println(x);
    x %= 2; // x = x%2
    System.out.println(x);
  }
}
