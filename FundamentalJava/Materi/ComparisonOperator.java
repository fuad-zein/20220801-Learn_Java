package FundamentalJava.Materi;

public class ComparisonOperator {
  public static void main(String[] args) {
    // Digunakan untuk menyatakan perbandingan dari 2 buah nilai.
    // Kembalian dari operator ini bernilai boolean (true or false)

    /*
     * == sama dengan
     * != tidak sama dengan
     * > lebih dari
     * >= lebih dari atau sama dengan
     * < kurang dari
     * <= kurang dari atau sama dengan
     */

    int x = 7;
    int y = 3;

    System.out.println(x == y);
    System.out.println(x != y);
    System.out.println(x > y);
    System.out.println(x >= y);
    System.out.println(x < y);
    System.out.println(x <= y);
  }
}
