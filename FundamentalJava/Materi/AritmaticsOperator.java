package FundamentalJava.Materi;

public class AritmaticsOperator {
  public static void main(String[] args) {
    /*
     * + = operator penjumlahan (digunakan juga untuk concate)
     * - = operator pengurangan
     * * = operator perkalian
     * / = operator pembagian
     * % = operator modulus (sisa hasil bagi)
     */

    int x = 7;
    int y = 3;

    System.out.println(x + y);
    System.out.println(x - y);
    System.out.println(x * y);
    System.out.println(x / y);
    System.out.println(x % y);
  }
}
