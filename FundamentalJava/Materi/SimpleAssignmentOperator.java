package FundamentalJava.Materi;

public class SimpleAssignmentOperator {
  public static void main(String[] args) {
    /*
     * SimpleAssignmentOperator = dengan syntax "=" maka memberikan nilai dari
     * sebelah kanan ke sebelah kirinya.
     */
    int angka2 = 96;
    float ipk2 = 3.5f;
    boolean isActive = false;
    String name2 = "Dev";

    System.out.println(angka2);
    System.out.println(ipk2);
    System.out.println(isActive);
    System.out.println(name2);
  }
}
